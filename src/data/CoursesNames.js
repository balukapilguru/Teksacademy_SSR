/**
 * Recommend up to 3 courses.
 *
 * INTEREST is a HARD filter — we never recommend a course outside the chosen
 * interest. Other criteria (goal, experience, time, budget) are scoring
 * signals so the user always sees courses from the area they picked.
 */
export function recommend(a) {
  const pool = a.interest
    ? courses.filter((c) => c.interests.includes(a.interest))
    : courses.slice();

  if (pool.length === 0) return [];

  const levelRank = { Beginner: 1, Intermediate: 2, Advanced: 3, Any: 2 };

  const scored = pool.map((c) => {
    let score = 0;
    if (a.interest && c.interests.includes(a.interest)) score += 10;
    if (a.goal && c.goals.includes(a.goal)) score += 4;
    if (a.experience && (c.level === a.experience || c.level === "Any")) score += 3;
    if (a.time && c.intensity.includes(a.time)) score += 3;
    if (a.budget && c.budget === a.budget) score += 2;

    if (a.experience) {
      const diff = Math.abs(levelRank[c.level] - levelRank[a.experience]);
      score += Math.max(0, 2 - diff);
    }
    return { c, score };
  });

  scored.sort((x, y) => y.score - x.score);
  return scored.slice(0, 3).map((s) => s.c);
}
